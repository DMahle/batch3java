package lecture_5_loop_practice;

public class Concatenation {
    public static void main(String[] args) {
        // Concatenation - intro
        // This function allows for strings to be put together with other strings or dtats types
        // we use the (+) operator to perform this function

        //string and string
        System.out.println("H"+"E"+"L"+"L"+"O"+"!");
        System.out.println("Hello"+" "+"World");

        //string and boolean
        System.out.println("This is" + false);
        System.out.println("This is line 18: value" + true);

        //string and integer
        System.out.println("The number is :" + 10);
        System.out.println("The number is :" +10+25);

        //string and double
        System.out.println("This number has a decimal:" + 1.09);

        //string and characters
        System.out.println("This is concatenation with the character" + '$');

    }


}
